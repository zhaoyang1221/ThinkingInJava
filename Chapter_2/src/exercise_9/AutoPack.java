package exercise_9;

public class AutoPack {
    public static void main(String[] args) {
        int i = 0;
        short s = 16;
        long l= 32;

        double d = 1.1314;
        float f = 234f;

        char c = 'a';
        byte b = 'b';

        boolean bool = false;

        Integer integer = i;
        System.out.println("int i = " + i);
        System.out.println("Integer i = " + integer);

        Short sh = s;
        System.out.println("short s =" + s);
        System.out.println("Short sh =" + sh);

        Long lo = l;
        System.out.println("long l =" + l);
        System.out.println("Long lo =" + lo);

        Double dou = d;
        System.out.println("double d =" + d);
        System.out.println("Double dou =" + dou);

        Float fl = f;
        System.out.println("float f = " + f);
        System.out.println("Float fl = " + fl);

        Character character = c;
        System.out.println("char c = "+ c);
        System.out.println("Character character = "+ character);

        Byte by = b;
        System.out.println("byte b = "+ b);
        System.out.println("Byte by = "+ by);

        Boolean boole = bool;
        System.out.println("boolean boole = " + bool);
        System.out.println("Boolean boole = " + boole);





    }
}
